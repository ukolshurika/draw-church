# Auto-generate compound entries for uyezd/volost/guberniya
def _generate_compound_entries() -> dict[str, str]:
    """Generate 'word + уезд/волость/губерния' entries from base synonyms."""
    compounds = {}
    for key, val in MANUAL_SYNONYMS.items():
        if val.endswith(" уезд") and "уезд" not in key and len(key.split()) == 1:
            compounds[f"{key} уезд"] = val
        elif val.endswith(" волость") and "волость" not in key and len(key.split()) == 1:
            compounds[f"{key} волость"] = val
        elif val.endswith(" губерния") and "губерния" not in key and len(key.split()) == 1:
            compounds[f"{key} губерния"] = val
    return compounds

MANUAL_SYNONYMS.update(_generate_compound_entries())


SETTLEMENT_TYPES = [
    "деревня", "село", "сельцо", "город", "слобода",
    "погост", "местечко", "хутор", "посад", "завод", "полк",
]


def _lookup(word: str) -> str | None:
    """Look up a word/phrase in the synonym map."""
    key = word.lower().strip()
    if key in MANUAL_SYNONYMS:
        return MANUAL_SYNONYMS[key]
    return None


def normalize_name(name: str) -> str:
    """Normalize a settlement name (without type prefix)."""
    canonical = _lookup(name)
    if canonical:
        return canonical

    # If not in synonym map, just ensure first letter is upper
    if name:
        return name[0].upper() + name[1:] if name[0].islower() else name
    return name


def canonicalize_settlement(raw: str | None) -> str | None:
    """Extract canonical settlement from a raw geo string."""
    if not raw:
        return None

    s = raw.strip()
    s_lower = s.lower()

    # Context references — can't resolve without original entry context
    if re.search(r'(та же|то же|тот же|той же)', s_lower):
        return s

    # Normalize full string first
    full_canon = _lookup(s)
    if full_canon:
        return full_canon

    # Split by commas
    parts = [p.strip() for p in re.split(r'[,·]', s)]

    # Find the settlement part (last part with a type+name pattern)
    settlement_part = None
    for part in reversed(parts):
        part_lower = part.lower()
        for stype in SETTLEMENT_TYPES:
            m = re.match(rf'({stype})\s+(.+?)$', part_lower)
            if m:
                sname = m.group(2).strip()
                canonical_name = normalize_name(sname)
                settlement_part = f"{stype.capitalize()} {canonical_name}"
                break
        if settlement_part:
            break

    if settlement_part:
        return settlement_part

    # No settlement type found — normalize whatever we have
    # Check each part for uyezd/volost/guberniya
    for part in parts:
        canon = _lookup(part)
        if canon:
            return canon

    # Last resort: just normalize the whole string
    return normalize_name(s)


def main():
    print("=" * 60)
    print("Geo-data normalization")
    print("=" * 60)

    nodes = json.loads(NODES_FILE.read_text(encoding="utf-8"))

    stats = defaultdict(int)
    changes = []

    for node in nodes:
        old = node.get("settlement")
        new = canonicalize_settlement(old)
        node["settlement"] = new

        if old != new:
            changes.append((old, new))
            stats["changed"] += 1
        else:
            stats["unchanged"] += 1

        if new is None and old is not None:
            stats["nullified"] += 1
        if any(x in (old or "") for x in ("та же", "то же", "тот же", "той же")):
            stats["context_ref"] += 1

    print(f"\nTotal nodes: {len(nodes)}")
    print(f"With settlement: {sum(1 for n in nodes if n.get('settlement'))}")
    print(f"Changed: {stats['changed']}")
    print(f"Unchanged: {stats['unchanged']}")
    print(f"Context refs (unresolved): {stats['context_ref']}")

    canonicals = defaultdict(int)
    for n in nodes:
        if n.get("settlement"):
            canonicals[n["settlement"]] += 1
    print(f"\nUnique canonical settlements: {len(canonicals)}")
    print("Top 30:")
    for s, c in sorted(canonicals.items(), key=lambda x: -x[1])[:30]:
        print(f"  {c:4d}  {s}")

    NODES_FILE.write_text(
        json.dumps(nodes, ensure_ascii=False, indent=2), encoding="utf-8"
    )
    print(f"\nSaved: {NODES_FILE}")

    existing = {}
    if SYNONYMS_FILE.exists():
        existing = json.loads(SYNONYMS_FILE.read_text(encoding="utf-8"))
    for k, v in MANUAL_SYNONYMS.items():
        if k not in existing:
            existing[k] = v
    SYNONYMS_FILE.write_text(
        json.dumps(existing, ensure_ascii=False, indent=2), encoding="utf-8"
    )
    print(f"Updated: {SYNONYMS_FILE} ({len(existing)} entries)")


if __name__ == "__main__":
    main()
